package utils;

/**
 *
 * @author Asus
 */
public class Personne {

    private int Id;
    private int Nom;
    private int Prenom;
    private int Adress;

    public int getId() {
        return Id;
    }

    public void setId(int Id) {
        this.Id = Id;
    }

    public int getNom() {
        return Nom;
    }

    public void setNom(int Nom) {
        this.Nom = Nom;
    }

    public int getPrenom() {
        return Prenom;
    }

    public void setPrenom(int Prenom) {
        this.Prenom = Prenom;
    }

    public int getAdress() {
        return Adress;
    }

    public void setAdress(int Adress) {
        this.Adress = Adress;
    }

}
